#include "../../lib/libft.h"
#include "../../inc/push_swap.h"

int	is_sorted(t_stack *stack)
{
	if (ft_stacklen(stack) < 2)
		return (1);
	while (stack->next)
	{
		if (stack->value > stack->next->value)
			return (0);
		stack = stack->next;
	}
	return (1);
}
